public class InstanceVariable {
    int x;
    boolean b;
    double d;
    String s;
    public static  void main(String[] args){
        InstanceVariable i = new InstanceVariable();
        System.out.println(i.x);
        System.out.println(i.b);
        System.out.println(i.s);
        System.out.println(i.d);
    }
}

